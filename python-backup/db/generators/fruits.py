def fruits(Model):
    # Excellent fruits
    great_fruits = [
        'Avocado',
        'Mango',
    ]

    for fruit in great_fruits:
        yield Model(name=f'Raw {fruit}', ingredients=[fruit], score=4)

    # Generally likes fruits
    for fruit in COMMON_FRUITS:
        yield Model(name=f'Raw {fruit}', ingredients=[fruit], score=3)

    # Other generic fruits
    for fruit in ALL_FRUITS:
        yield Model(name=f'Raw {fruit}', ingredients=[fruit], score=2)
